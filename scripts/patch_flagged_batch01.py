def main():
    with open(CSV, encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = list(reader.fieldnames)
        rows = list(reader)

    done_cnt = update_cnt = 0

    for row in rows:
        rid = row["record_id"]

        if rid in DONE_RIDS:
            row["human_check_status"] = "done"
            row["final_status"] = "reviewed"
            row["issue_flag"] = "0"
            row["issue_type"] = ""
            row["issue_detail"] = ""
            row["human_check_note"] = DONE_NOTE
            done_cnt += 1

        elif rid in FLAG_UPDATE:
            row["human_check_note"] = FLAG_UPDATE[rid]
            update_cnt += 1

    with open(CSV, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"done/reviewed へ昇格 : {done_cnt} 件")
    print(f"フラグノート更新     : {update_cnt} 件")
    print(f"合計処理             : {done_cnt + update_cnt} 件")


if __name__ == "__main__":
    main()
