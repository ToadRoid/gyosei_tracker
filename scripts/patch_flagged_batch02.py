def main():
    with open(CSV, encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = list(reader.fieldnames)
        rows = list(reader)

    updated = 0
    for row in rows:
        rid = row["record_id"]
        if rid in FLAG_NOTE_UPDATES:
            row["human_check_note"] = FLAG_NOTE_UPDATES[rid]
            updated += 1

    with open(CSV, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"フラグノート更新: {updated} 件")
    remaining = sum(1 for r in rows if r["human_check_status"] == "flagged")
    print(f"flagged 残件数  : {remaining} 件 (変更なし)")


if __name__ == "__main__":
    main()
