def main():
    with open(CSV, encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = list(reader.fieldnames)
        rows = list(reader)

    done_cnt = flag_cnt = 0

    for row in rows:
        rid = row["record_id"]

        if rid in DONE_RIDS:
            row["human_check_status"] = "done"
            row["final_status"] = "reviewed"
            row["issue_flag"] = "0"
            row["issue_type"] = ""
            row["issue_detail"] = ""
            row["human_check_note"] = (
                "読取揺れ → 実テキスト確認済。"
                "question_text・answer_symbol・explanation_text の法的整合確認。"
            )
            done_cnt += 1

        elif rid in FLAG_PATCHES:
            row["human_check_status"] = "flagged"
            row["final_status"] = "draft"
            row["issue_flag"] = "1"
            row["issue_type"] = "読取揺れ"
            row["human_check_note"] = FLAG_PATCHES[rid]
            flag_cnt += 1

    with open(CSV, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"done/reviewed : {done_cnt} 件")
    print(f"flagged       : {flag_cnt} 件")
    print(f"合計           : {done_cnt + flag_cnt} 件")


if __name__ == "__main__":
    main()
